package com.ghost.writing.components.dto;

import java.util.Collection;

import com.ghost.writing.components.dao.IntellectualCharacteristics;

/**
 * 
 * @author toyan
 * 
 * Character profile mapping character's intellectual characteristics.
 *
 */
public class IntellectualCharacteristicsDto implements IntellectualCharacteristics {
	private String educationalBackground;
	private String intelligenceLevel;
	private Collection<String> learningExperiences;
	private Collection<String> mentalIllnesses;
	private Collection<String> shortTermLifeGoals;
	private Collection<String> longTermLifeGoals;
	//how does character see himself/herself?
	private String reflection;
	//how does character believe he or she is perceived by others?
	private String perceptionFromOthers;
	private String selfConfident;
	//does the character seem ruled by emotion or logic or some combination thereof?
	private String emotionalLogical;
	//what would most embarrass this character
	private String embarrassment;
	
	
	/**
	 * @return the educationalBackground
	 */
	public String getEducationalBackground() {
		return educationalBackground;
	}
	/**
	 * @param educationalBackground the educationalBackground to set
	 */
	public void setEducationalBackground(String educationalBackground) {
		this.educationalBackground = educationalBackground;
	}
	/**
	 * @return the intelligenceLevel
	 */
	public String getIntelligenceLevel() {
		return intelligenceLevel;
	}
	/**
	 * @param intelligenceLevel the intelligenceLevel to set
	 */
	public void setIntelligenceLevel(String intelligenceLevel) {
		this.intelligenceLevel = intelligenceLevel;
	}
	/**
	 * @return the learningExperiences
	 */
	public Collection<String> getLearningExperiences() {
		return learningExperiences;
	}
	/**
	 * @param learningExperiences the learningExperiences to set
	 */
	public void setLearningExperiences(Collection<String> learningExperiences) {
		this.learningExperiences = learningExperiences;
	}
	/**
	 * @return the mentalIllnesses
	 */
	public Collection<String> getMentalIllnesses() {
		return mentalIllnesses;
	}
	/**
	 * @param mentalIllnesses the mentalIllnesses to set
	 */
	public void setMentalIllnesses(Collection<String> mentalIllnesses) {
		this.mentalIllnesses = mentalIllnesses;
	}
	/**
	 * @return the shortTermLifeGoals
	 */
	public Collection<String> getShortTermLifeGoals() {
		return shortTermLifeGoals;
	}
	/**
	 * @param shortTermLifeGoals the shortTermLifeGoals to set
	 */
	public void setShortTermLifeGoals(Collection<String> shortTermLifeGoals) {
		this.shortTermLifeGoals = shortTermLifeGoals;
	}
	/**
	 * @return the longTermLifeGoals
	 */
	public Collection<String> getLongTermLifeGoals() {
		return longTermLifeGoals;
	}
	/**
	 * @param longTermLifeGoals the longTermLifeGoals to set
	 */
	public void setLongTermLifeGoals(Collection<String> longTermLifeGoals) {
		this.longTermLifeGoals = longTermLifeGoals;
	}
	/**
	 * @return the reflection
	 */
	public String getReflection() {
		return reflection;
	}
	/**
	 * @param reflection the reflection to set
	 */
	public void setReflection(String reflection) {
		this.reflection = reflection;
	}
	/**
	 * @return the perceptionFromOthers
	 */
	public String getPerceptionFromOthers() {
		return perceptionFromOthers;
	}
	/**
	 * @param perceptionFromOthers the perceptionFromOthers to set
	 */
	public void setPerceptionFromOthers(String perceptionFromOthers) {
		this.perceptionFromOthers = perceptionFromOthers;
	}
	/**
	 * @return the selfConfident
	 */
	public String getSelfConfident() {
		return selfConfident;
	}
	/**
	 * @param selfConfident the selfConfident to set
	 */
	public void setSelfConfident(String selfConfident) {
		this.selfConfident = selfConfident;
	}
	/**
	 * @return the emotionalLogical
	 */
	public String getEmotionalLogical() {
		return emotionalLogical;
	}
	/**
	 * @param emotionalLogical the emotionalLogical to set
	 */
	public void setEmotionalLogical(String emotionalLogical) {
		this.emotionalLogical = emotionalLogical;
	}
	/**
	 * @return the embarrassment
	 */
	public String getEmbarrassment() {
		return embarrassment;
	}
	/**
	 * @param embarrassment the embarrassment to set
	 */
	public void setEmbarrassment(String embarrassment) {
		this.embarrassment = embarrassment;
	}

}
